# tuplas são utilizadas para armazenar dados em uma mesma variavel
# Elas são declaradas como uma variável comum, apenas sendo sinalizada com ()
# A diferença dela para as listas é que tuplas são imutaveis, não podem ser alteradas de nenhuma forma pelo programa

# nome ilustrativo, pode ser utilizado qualquer nome
tupla = ("banana", "maçã", 'uva')

# print comum de uma tupla
print(tupla)

# acessando de forma separada cada item
print(tupla[0], tupla[1], tupla[2])

# Não existem funções built-in de manipulação de tuplas